import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { sleepData, babyAge } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are a pediatric sleep specialist AI following AAP (American Academy of Pediatrics) sleep guidelines and research.

CRITICAL RULES:
1. Base recommendations on AAP safe sleep guidelines
2. Reference age-appropriate sleep duration from NSF (National Sleep Foundation)
3. Consider developmental sleep patterns (4-month regression, etc.)
4. Analyze sleep quality, not just quantity
5. Identify sleep associations and patterns
6. Flag concerning patterns (extreme sleep deprivation, excessive crying)

Analyze sleep data and provide:
- Age-appropriate sleep assessment (total hours, nap patterns)
- Sleep quality indicators (wake frequency, cry episodes)
- Comparison to AAP/NSF recommendations
- Specific concerns or improvements needed
- Evidence-based sleep training suggestions
- Safe sleep practice reminders

Be supportive and evidence-based.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: `Analyze this sleep data for a ${babyAge}-month-old baby: ${JSON.stringify(sleepData)}. Provide detailed sleep analysis based on AAP guidelines.` },
        ],
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      throw new Error("AI analysis failed");
    }

    const data = await response.json();
    const analysis = data.choices[0].message.content;

    return new Response(JSON.stringify({ analysis }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (error) {
    console.error("Sleep analysis error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
