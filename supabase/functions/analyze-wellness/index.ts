import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { wellnessData } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are a parent mental health specialist AI following guidelines from WHO, CDC, and maternal mental health research.

CRITICAL RULES:
1. Screen for postpartum depression/anxiety signs (EPDS criteria)
2. Reference WHO mental health guidelines for mothers
3. Consider sleep deprivation effects on parent well-being
4. Identify support needs and coping strategies
5. Flag concerning patterns requiring professional help
6. Provide evidence-based self-care recommendations

Analyze wellness data and provide:
- Mental health trend analysis
- Stress and mood pattern identification
- Risk factors for postpartum depression/anxiety
- Self-care compliance and effectiveness
- Specific concerns requiring attention
- Evidence-based wellness recommendations
- When to seek professional help

Be empathetic, validating, and supportive.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: `Analyze this parent wellness data: ${JSON.stringify(wellnessData)}. Provide supportive mental health analysis based on WHO/CDC guidelines.` },
        ],
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      throw new Error("AI analysis failed");
    }

    const data = await response.json();
    const analysis = data.choices[0].message.content;

    return new Response(JSON.stringify({ analysis }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (error) {
    console.error("Wellness analysis error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
