import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { nutritionData, babyAge } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    const systemPrompt = `You are a pediatric nutrition specialist AI following WHO infant feeding guidelines and AAP nutrition recommendations.

CRITICAL RULES:
1. Follow WHO breastfeeding and complementary feeding guidelines
2. Reference AAP nutrition policies and iron recommendations
3. Consider age-appropriate foods and developmental readiness
4. Identify potential allergens and introduction timing (AAP guidelines)
5. Assess nutritional adequacy (iron, vitamin D, protein, etc.)
6. Flag concerning patterns (inadequate intake, food aversions)

Analyze nutrition data and provide:
- Age-appropriate feeding assessment
- Nutritional adequacy evaluation
- Food variety and balance analysis
- Allergen introduction tracking
- Specific nutritional gaps or concerns
- Evidence-based feeding recommendations
- Supplement needs (vitamin D, iron)

Be practical and supportive.`;

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: systemPrompt },
          { role: "user", content: `Analyze this nutrition data for a ${babyAge}-month-old baby: ${JSON.stringify(nutritionData)}. Provide detailed nutritional analysis based on WHO/AAP guidelines.` },
        ],
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      throw new Error("AI analysis failed");
    }

    const data = await response.json();
    const analysis = data.choices[0].message.content;

    return new Response(JSON.stringify({ analysis }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch (error) {
    console.error("Nutrition analysis error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      }
    );
  }
});
